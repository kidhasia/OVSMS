package admin;




	public class FeedBack {
		private int f_ID;
		private String f_name;
		private String f_conNum;
		private String f_email;
		private String f_feedBack;
		public FeedBack(int f_ID, String f_name, String f_conNum, String f_email, String f_feedBack) {
			super();
			this.f_ID = f_ID;
			this.f_name = f_name;
			this.f_conNum = f_conNum;
			this.f_email = f_email;
			this.f_feedBack = f_feedBack;
		}
		public int getF_ID() {
			return f_ID;
		}
		public void setF_ID(int f_ID) {
			this.f_ID = f_ID;
		}
		public String getF_name() {
			return f_name;
		}
		public void setF_name(String f_name) {
			this.f_name = f_name;
		}
		public String getF_conNum() {
			return f_conNum;
		}
		public void setF_conNum(String f_conNum) {
			this.f_conNum = f_conNum;
		}
		public String getF_email() {
			return f_email;
		}
		public void setF_email(String f_email) {
			this.f_email = f_email;
		}
		public String getF_feedBack() {
			return f_feedBack;
		}
		public void setF_feedBack(String f_feedBack) {
			this.f_feedBack = f_feedBack;
		}
		
		
		
	
}
